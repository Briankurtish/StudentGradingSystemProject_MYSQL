    /*
    DateFormatConverter will convert the date format 
    from "May 15, 2021" to 15/05/2021
    */
    public String DateFormatConverter(String s_date) {
    String dd=null, mm=null, yyyy=null;
    int w_start=0;
    for (int i=0; i<s_date.length(); i++) {
        if(s_date.charAt(i)==' ') {
           if(mm==null) {
               mm= s_date.substring(w_start, i);
               w_start=i+1;
           } else if (dd==null) {
               dd=s_date.substring(w_start, i-1); // Do not get "," character
               w_start=i+1;
           }
       }
    }  
       yyyy=s_date.substring(w_start, s_date.length());
       return dd+"/"+mm+"/"+yyyy;    
    }
    
    
 
    
    
    
    public void refresh_JTable() {
        
           List students = StudentGradingSystemProject.students;
           DefaultTableModel model = (DefaultTableModel) studTable.getModel();
           Object rowData[] = new Object[7]; 
           Iterator itr = students.iterator();
           Student std;          
           model.setNumRows(0); //Remove all existing rows of JTable
           //Add every element of the List Array as a new row into JTable
           while (itr.hasNext()) {
              std = (Student) itr.next();
              rowData[0] = std.getStd_id();
              rowData[1] = std.getStd_no();
              rowData[2] = std.getStd_name();
              rowData[3] = std.getStd_surname();
              rowData[4] = std.getGender();
              rowData[5] = std.getNationality();
              rowData[6] = StudentGradingSystemProject.fmt.format(std.getBirthday().getTime());  
              //rowData[6] = std.getStd_birthdate();  
              model.addRow(rowData);
           }   
          }
   
    
