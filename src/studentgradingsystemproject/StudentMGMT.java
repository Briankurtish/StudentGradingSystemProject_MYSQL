    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {                                        
        // TODO add your handling code here:
        if(
           std_id.getText().trim().isEmpty()&&
           std_name.getText().trim().isEmpty()&&
           std_num.getText().trim().isEmpty()&&
           std_surname.getText().trim().isEmpty()&&
           nationality.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "You have to fill all fields before saving!");
            }                                        
            else{
        try{
            con = DriverManager.getConnection("jdbc:mysql://localhost/schoolgradingsystem", "root", "root");
            PreparedStatement add = con.prepareStatement("insert into student values(?,?,?,?,?,?,?)");
            add.setString(1, std_id.getText());
            add.setString(2, std_num.getText());
            add.setString(3, std_name.getText());
            add.setString(4, std_surname.getText());
            
            char std_gender;
            if(femaleRadio.isSelected()) std_gender='F'; else std_gender='M';
            // Convert char to String
            String genderString = Character.toString(std_gender);
            String temp_date = datePicker2.getDate().toString().trim();
            
            add.setString(5, genderString);
            add.setString(6, nationality.getText());
            add.setString(7, temp_date);
            int row = add.executeUpdate();
            JOptionPane.showMessageDialog(this, "Student Added Successfully");
            DisplayStudents();
            Reset();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "An Error Occured");
            e.printStackTrace();
        }}
        
    }
    
