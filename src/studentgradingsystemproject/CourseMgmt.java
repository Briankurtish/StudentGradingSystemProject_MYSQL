    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {                                        
        // TODO add your handling code here:
        if(
           crs_id.getText().trim().isEmpty()&&
           crs_code.getText().trim().isEmpty()&&
           crs_name.getText().trim().isEmpty()&&
           dept_id.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "You have to fill all fields before saving!");
            }                                        
            else{
        try{
            con = DriverManager.getConnection("jdbc:mysql://localhost/schoolgradingsystem", "root", "root");
            PreparedStatement add = con.prepareStatement("insert into course values(?,?,?,?)");
            add.setString(1, crs_id.getText());
            add.setString(2, dept_id.getText());
            add.setString(3, crs_code.getText());
            add.setString(4, crs_name.getText());
            
            int row = add.executeUpdate();
            JOptionPane.showMessageDialog(this, "Course Added Successfully");
            DisplayCourses();
            Reset();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "An Error Occured");
            e.printStackTrace();
        }}
    }                                       

